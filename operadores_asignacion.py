#Operadores de Asignación 

x = 5
print(x)
x = x + 2 # Suma y asignación
x += 2 # Forma que se debe usar
print(x)

x = x - 3 # Resta y asignación
x -= 3 # Forma que se debe usar
print(x)

x = x * 4 # Multiplicación y asignación
x *= 4 # Forma que se debe usar 
print(x)

x = x / 2 # División y asignación
x /= 2 # Forma que se debe usar
print(x)

x = x % 3 # Módulo y asignación
x %= 3 # Forma que se debe usar
print(x)

x = x // 2 # División entera y asignación
x //= 2 # Forma que se debe usar
print(x)

x = x ** 3 # Potencia y asignación
x **= 3 # Forma que se debe usar
print(x)


x = 10 # Asigne la variable x con el valor 10
print(x) # Imprima el valor de x

# Walrus operator (Python 3.8+) :=

print(x := 10) # Asigna el valor 10 a x y lo imprime al mismo tiempo (x)
